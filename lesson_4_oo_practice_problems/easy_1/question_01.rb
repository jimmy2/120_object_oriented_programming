# Which of the following are objects in Ruby? If they are objects, 
# how can you find out what class they belong to?

# true
# "hello"
# [1, 2, 3, "happy days"]
# 142

# Everything in ruby is an object.

p true.class
p "hello".class
p [1, 2, 3, "happy days"].class
p 142.class

# => TrueClass
# => String
# => Array
# => Fixnum